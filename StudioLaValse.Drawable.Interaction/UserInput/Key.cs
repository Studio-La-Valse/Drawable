namespace StudioLaValse.Drawable.Interaction.UserInput
{
    /// <summary>
    /// Known keyboard keys
    /// </summary>
    public enum Key
    {
        /// <summary>
        /// Key not recognized
        /// </summary>
        Unknown,
        /// <summary>
        /// The default shift key
        /// </summary>
        Shift,
        /// <summary>
        /// The default control key
        /// </summary>
        Control,
        /// <summary>
        /// The default escape key
        /// </summary>
        Escape,
        /// <summary>
        /// The default z key
        /// </summary>
        Z,
        /// <summary>
        /// the default r key
        /// </summary>
        R,
        /// <summary>
        /// The delete key
        /// </summary>
        Delete
    }
}
